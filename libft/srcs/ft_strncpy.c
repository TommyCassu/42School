#include "libft.h"

char    *ft_strncpy(char *dest, char *src, size_t n)
{
    size_t i;

    i = 0;
    while (src[i] != '\0' && i < n - 1)
    {
        dest[i] = src[i];
        i++;
    }
    dest[i] = '\0';
    return (dest);
}