#include "libft.h"

void    ft_putstr_fd(char *s, int fd)
{
    int i;

    i = 0;
    if (!s)
        return (NULL);
    while (s[i] != '\0')
    {
        ft_putchar(s[i], fd);
        i++;
    }
}